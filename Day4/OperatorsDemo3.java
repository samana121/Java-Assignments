package Day4;

public class OperatorsDemo3 {
    public static void main(String[] args) {
        //Relational Operator
        // > < = <= == !=
        int a = 5;
        int b = 6;
        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a >= b);
        System.out.println(a <= b);
        System.out.println(a == b);
        System.out.println(a != b);


    }

}
