package Day4;

public class LiteralsDemo {
    public static void main(String[] args) {
        //Literals fixed value + data type

        //1. Integer Literal 3453 77 9786987
        byte b = 127;
        short s = 32767;
        int i = 2147483647;

        //2.floating literals 45. 67. 87.(double)
        float f = -23.45F;
        double d = 23.45;

        //3. boolean literals
        boolean b1 = true;
        boolean b2 = false;

        //4. character literals

        char c1 = 'a';
        char c2 = '1';
        char c3 = '?';
        char c4 = '\u0000';

        //5.String
        String a1 = "Nepal";
        String s2 = " My name is Samana";
        String s3 = "a";
        String s4 = "";
        String s5 = " ";
        String s6 = "123";
        String s7 = "true";


        //6. Null Literals
        String s10 = null;
        LiteralsDemo ob = new LiteralsDemo(); //This is an object in java
        LiteralsDemo obj = null;












    }
}
